import pandas as pd
df=pd.read_csv("/content/ecommerce.csv")
print(df.head())
print(df.info())
print(df.describe())

print(df.isnull().sum())

import seaborn as sns
import matplotlib.pyplot as plt

corr = df.corr(numeric_only=True)

plt.figure(figsize=(8,6))
sns.heatmap(corr, annot=True, cmap='coolwarm')
plt.title("Correlation Matrix")
plt.show()

sns.scatterplot(x='Discount', y='Profit', data=df)

plt.title("Discount vs Profit")
plt.show()

product_profit = df.groupby('Product')['Profit'].sum().sort_values()

print(product_profit)
product_profit.plot(kind='barh', figsize=(10,8))

plt.title("Profit by Product")
plt.xlabel("Profit")
plt.show()

sns.boxplot(x='Payment_method', y='Sales', data=df)

plt.title("Sales by Payment Method")
plt.show()

sns.boxplot(x='Order_Priority', y='Shipping_Cost', data=df)

plt.title("Shipping Cost by Order Priority")
plt.show()

sns.scatterplot(x='Quantity', y='Profit', data=df)

plt.title("Quantity vs Profit")
plt.show()